package DataTypesJava;

public class DataTypes {
	public static void main(String[]args){
	byte a = -128;  				          // range -128 to 127
	byte b = 127;
	
	short c = -32768;
	short d = 32767;   			    	  // range -32768 to 32767
	
	int e = -2147483647;
	int f = 2147483646;						// range -2147483647 to 2147483646
	
	long g = -9223372036854775807l;  		// rang -9223372036854775807l to 9223372036854775808l	
	long h =  9223372036854775806l;
	
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	System.out.println(f);
	System.out.println(g);
	System.out.println(h);
	}
}
